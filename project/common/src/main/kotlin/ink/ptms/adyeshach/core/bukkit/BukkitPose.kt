package ink.ptms.adyeshach.core.bukkit

/**
 * 表示 Minecraft 实体的各种姿态状态的枚举类
 *
 * @author Arasple
 * @date 2020/8/23 18:53
 */
enum class BukkitPose {

    /**
     * 站立状态 - 实体处于直立的默认姿态
     * 适用于: 几乎所有实体
     */
    STANDING,

    /**
     * 滑翔状态 - 实体使用鞘翅(Elytra)飞行时的姿态
     * 适用于: 玩家
     */
    FALL_FLYING,

    /**
     * 睡眠状态 - 实体在床上或指定地点睡觉时的姿态
     * 适用于: 玩家、狐狸、猫、村民等
     */
    SLEEPING,

    /**
     * 游泳状态 - 实体在水中游泳时的姿态
     * 适用于: 玩家、大多数生物
     */
    SWIMMING,

    /**
     * 旋转攻击状态 - 实体进行旋转攻击动作时的姿态
     * 适用于: 玩家(使用三叉戟旋转攻击时)
     */
    SPIN_ATTACK,

    /**
     * 潜行状态 - 实体处于潜行(按住Shift)时的姿态
     * 适用于: 玩家、猫等
     */
    SNEAKING,

    /**
     * 长跳状态 - 实体进行较长距离跳跃时的姿态
     * 适用于: 玩家(使用山羊角时)、山羊
     */
    LONG_JUMPING,

    /**
     * 濒死状态 - 实体处于死亡动画或濒死状态时的姿态
     * 适用于: 几乎所有生物实体
     */
    DYING,

    /**
     * 呱呱叫状态 - 青蛙实体发出呱呱声时的姿态
     * 适用于: 青蛙
     */
    CROAKING,

    /**
     * 使用舌头状态 - 青蛙实体伸出舌头捕食时的姿态
     * 适用于: 青蛙
     */
    USING_TONGUE,

    /**
     * 坐下状态 - 实体处于坐下姿态
     * 适用于: 狼、猫、鹦鹉、熊猫等宠物或可驯服生物
     */
    SITTING,

    /**
     * 咆哮状态 - 实体发出吼叫或咆哮时的姿态
     * 适用于: 熊、监守者(Warden)等
     */
    ROARING,

    /**
     * 嗅探状态 - 实体进行嗅探动作时的姿态
     * 适用于: 狼、监守者(Warden)、嗅探兽(Sniffer)
     */
    SNIFFING,

    /**
     * 浮现状态 - 实体从某处浮现或出现时的姿态
     * 适用于: 监守者(Warden)从方块中出现时
     */
    EMERGING,

    /**
     * 挖掘状态 - 实体进行挖掘动作时的姿态
     * 适用于: 嗅探兽(Sniffer)挖掘种子时
     */
    DIGGING,

    /**
     * 滑行状态 - 实体在冰或滑行表面上滑动时的姿态
     * 适用于: 玩家、大多数生物在冰上滑行
     */
    SLIDING,

    /**
     * 射击状态 - 实体进行射击动作时的姿态
     * 适用于: 骷髅、凋灵骷髅、卫道士等弓箭型生物
     */
    SHOOTING,

    /**
     * 吸入状态 - 实体准备释放声波攻击时的姿态
     * 适用于: 监守者(Warden)在释放声波攻击前的蓄力状态
     */
    INHALING;
}